package com.herinoid.rsi.model;

/**
 *
 * @author Hewrei
 */
public class PemeriksaanRalan {
    private String tinggiBadan;
    private String beratBadan;
    private String suhuTubuh;
    private String tekanDarah;
    private String alergi;
    private String usia;
    
    public PemeriksaanRalan(){
        
    }

    /**
     * @return the tinggiBadan
     */
    public String getTinggiBadan() {
        return tinggiBadan;
    }

    /**
     * @param tinggiBadan the tinggiBadan to set
     */
    public void setTinggiBadan(String tinggiBadan) {
        this.tinggiBadan = tinggiBadan;
    }

    /**
     * @return the beratBadan
     */
    public String getBeratBadan() {
        return beratBadan;
    }

    /**
     * @param beratBadan the beratBadan to set
     */
    public void setBeratBadan(String beratBadan) {
        this.beratBadan = beratBadan;
    }

    /**
     * @return the suhuTubuh
     */
    public String getSuhuTubuh() {
        return suhuTubuh;
    }

    /**
     * @param suhuTubuh the suhuTubuh to set
     */
    public void setSuhuTubuh(String suhuTubuh) {
        this.suhuTubuh = suhuTubuh;
    }

    /**
     * @return the tekanDarah
     */
    public String getTekanDarah() {
        return tekanDarah;
    }

    /**
     * @param tekanDarah the tekanDarah to set
     */
    public void setTekanDarah(String tekanDarah) {
        this.tekanDarah = tekanDarah;
    }

    /**
     * @return the alergi
     */
    public String getAlergi() {
        return alergi;
    }

    /**
     * @param alergi the alergi to set
     */
    public void setAlergi(String alergi) {
        this.alergi = alergi;
    }

    /**
     * @return the usia
     */
    public String getUsia() {
        return usia;
    }

    /**
     * @param usia the usia to set
     */
    public void setUsia(String usia) {
        this.usia = usia;
    }
    
    
    
}
