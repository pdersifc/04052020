package com.herinoid.rsi.model;

/**
 *
 * @author Hewrei
 */
public class RekapPembelianObat {
    private String noFaktur;
    private String tanggalBeli;
    private String namaSuplier;
    private String jumlah;
    private String ppn;
    private String total;
    private String namaPetugas;
    private String lokasi;
    private String diskon;
    
    public RekapPembelianObat(){}

    /**
     * @return the noFaktur
     */
    public String getNoFaktur() {
        return noFaktur;
    }

    /**
     * @param noFaktur the noFaktur to set
     */
    public void setNoFaktur(String noFaktur) {
        this.noFaktur = noFaktur;
    }

    /**
     * @return the tanggalBeli
     */
    public String getTanggalBeli() {
        return tanggalBeli;
    }

    /**
     * @param tanggalBeli the tanggalBeli to set
     */
    public void setTanggalBeli(String tanggalBeli) {
        this.tanggalBeli = tanggalBeli;
    }

    /**
     * @return the namaSuplier
     */
    public String getNamaSuplier() {
        return namaSuplier;
    }

    /**
     * @param namaSuplier the namaSuplier to set
     */
    public void setNamaSuplier(String namaSuplier) {
        this.namaSuplier = namaSuplier;
    }

    /**
     * @return the jumlah
     */
    public String getJumlah() {
        return jumlah;
    }

    /**
     * @param jumlah the jumlah to set
     */
    public void setJumlah(String jumlah) {
        this.jumlah = jumlah;
    }

    /**
     * @return the ppn
     */
    public String getPpn() {
        return ppn;
    }

    /**
     * @param ppn the ppn to set
     */
    public void setPpn(String ppn) {
        this.ppn = ppn;
    }

    /**
     * @return the total
     */
    public String getTotal() {
        return total;
    }

    /**
     * @param total the total to set
     */
    public void setTotal(String total) {
        this.total = total;
    }

    /**
     * @return the namaPetugas
     */
    public String getNamaPetugas() {
        return namaPetugas;
    }

    /**
     * @param namaPetugas the namaPetugas to set
     */
    public void setNamaPetugas(String namaPetugas) {
        this.namaPetugas = namaPetugas;
    }

    /**
     * @return the lokasi
     */
    public String getLokasi() {
        return lokasi;
    }

    /**
     * @param lokasi the lokasi to set
     */
    public void setLokasi(String lokasi) {
        this.lokasi = lokasi;
    }

    /**
     * @return the diskon
     */
    public String getDiskon() {
        return diskon;
    }

    /**
     * @param diskon the diskon to set
     */
    public void setDiskon(String diskon) {
        this.diskon = diskon;
    }

    
    
}
